		<h2>Biscuits and sweets </h2>
		<hr> 

		The Netherlands is a country where a lot of cake and sweets are eaten. Something all Dutch people eat are stroopwafels, which are flat biscuits with syrup in between. And part of the Dutch eat liquorice, but a large part does not. Liquorice is a type of candy you really have to love. On King's Day, the Dutch eat an orange tompouce, which is two slices of puff pastry with pastry cream in between and a thin layer of glaze on top. During Saint Nicholas, Dutch eat kruidnoten and pepernoten, which are a kind of small speculaas. Stuffed cakes are also very popular, these are cakes filled with spice. Candy crayons are also very popular, these are a kind of school board crayons but in candy form. These sweets have a kind of liquorice flavour.   
		<hr>
		<h2>Breakfast and lunch</h2>  
		<hr>
		In the morning, most people start with a sandwich. This is often topped with Dutch cheese and sprinkles. Hagelslag are a kind of mini chocolate granules, so this is really typical Dutch. You can buy the cheese in Gouda and Alkmaar. These 2 cities are very well known in the Netherlands for their good cheese. For lunch, small pancakes are sometimes on the menu. This is something the Dutch do not eat by default, but occasionally people eat it for lunch. The small pancakes are called poffertjes. Poffertjes are also sometimes eaten as snacks.   
		<hr>
		<h2>Dinner</h2>  
		<hr>
		For dinner, the Dutch have several options. The Dutch also often eat Italian, Japanese and Spanish. But the food the Dutch often eat is an avg'tje. That is potatoes, meat and vegetables. The vegetables and meat here vary every day. Stampot is also a good option. These are mashed potatoes with kale and endive, but there are many different types of kale in the Netherlands. In winter, the Dutch eat pea soup (snert) as a soup. These are eaten with French bread or pancakes, for example. In this, every Dutch person differs.   
		<hr>
		<h2>Snacks</h2>  <hr>
		Something you only come across in the Netherlands is a snack wall, this one with all kinds of snacks. You pay and choose a snack. After you have chosen the snack, click on the button next to/above the snack. The flap will now open, then you can grab the snack. The snack wall contains snacks such as a frikandel, cheese souffl√© and croquette. Also, bitterballen are often eaten with drinks. This snack has a crunchy edge and is filled with ragout. Kibbeling is another snack that the Dutch eat a lot. Kibbeling is kabbeljouw with a crispy edge. Kibbeling is also sometimes eaten for lunch.   
		<hr>
		<h1>Delicacies in provinces</h1> 
		<hr>
		 <h2>North Holland </h2> 
		<hr>
		Bokkenpootjes are typically North Holland, this is a biscuit made up of meringues of almond foam. These meringues are connected with butter cream. The sides are dipped in a layer of chocolate. You can also get herring in Volendam; herring is available in many fishing villages. Another fishing village is Urk, located in Flevoland. Zaanse mustard and Zaanse mayonnaise are also typically North Dutch. Also, Beemster cheese is typically North Dutch this is a type of cheese that is available everywhere, but comes from the Beemster. This makes it North Dutch.  
		<hr>
		<h2>South Holland </h2> 
		<hr>
		Something that comes from Rotterdam, which is in South Holland, is the kapsalon. Kapsalon contains chips, kebab or chicken, cheese, lettuce and sometimes tomato and cucumber. Hague hops are also typically South Dutch, coming from den Haag.   
		<hr>
		<h2>Zeeland</h2>  
		<hr>
		When you say food and Zeeland, almost every Dutch person thinks of a bolus. This is a sweet bite, as it is a sweet sugared roll. You can also spread butter on the bolus, but you don't have to. Zeeland is the province of water. So it is not very surprising that mussels, oysters and shrimps are typically Zeelandic.   
		<hr>
		<h2>Limburg  </h2>
		<hr>
		Limburg is the province of flans. If you really want to taste a good flan, you really need to be in Limburg! You can find flans in several supermarkets, but for the real old-fashioned flan you really have to go to Limburg.   
		<hr>
		<h2>Overijssel   </h2>
		<hr>
		A speciality from Overijssel is the Deventer cake. This is a kind of gingerbread, but of higher quality. It used to be baked according to strict municipal instructions, which allowed the Deventer cake to be kept longer.  
		<hr>
		<h2>Utrecht  </h2>
		<hr>
		Prauwels are biscuits commonly eaten in Utrecht. These are flat, thin biscuits and they resemble stroopwafels. Vijfschaft is also served for dinner. This is potatoes, carrots, onions, apples and brown beans. This is a typical winter dish and is eaten with bacon and smoked sausage.  
		<hr>
		<h2>North Brabant </h2> 
		<hr>
		A Bossche Bol is part of Noord-Brabant. This is a choux pastry filled with fresh whipped cream.  Dipped in chocolate. This is a very mighty pastry. If you really want to eat a good Bossche Bol, you should visit Den Bosch. In fact, here every restaurant has a Bossche Bol on its menu. This is because the Bossche Bol on the menu comes from the local baker and that baker is the best Bossche Bol baker in the Netherlands. Sausage rolls are also very popular, this is a bread dough (white) filled with spiced minced meat.  
		<hr>
		<h2>Gelderland  </h2>
		<hr>
		Arnhem girls is a traditional biscuit from Gelderland. This is a crunchy biscuit with a sugar coating. It also contains cinnamon.   
		<hr>
		<h2>Drenthe  </h2>
		<hr>
		In Drenthe, dry sausage is very well known among many people. This sausage is also called sexton sausage. It is a long-dried sausage. Farmers' waffles also used to be eaten in Drenthe. This is eaten for lunch. It is a rusk dipped in egg and milk and then fried in a frying pan. This is eaten with brown sugar. This dish is a bit like French toast. This is also a typical Dutch dish.   
		<hr>
		<h2>Flevoland  </h2>
		<hr>
		Flevosap is typically Dutch, Flevoland was added to the Netherlands later so has lesser delicacies. But you can only buy Urkse dikkoek in Flevoland. It comes from Urk, which has been around a bit longer. Urkse dikkoek is a cake made with flour, sultanas, butter, bacon and eggs.  
		<hr>
		<h2>Friesland  </h2>
		<hr>
		Duimpjes are typically Frisian and they are no bigger than a thumb. Duimpjes are made with ginger, cinnamon and aniseed. This is also why it has a spicy taste. Sugar bread also comes from Friesland, but nowadays it can be found in other provinces as well. Suikerbrood is a bread with cinnamon and sugar and is very sweet. The Dutch often eat it with butter.  
		<hr>
		<h2>Groningen   </h2>
		<hr>
		Groninger cake is very famous in this province and is a kind of breakfast cake. It is a spiced cake originally eaten with coffee. Its main ingredients are syrup and rye. Furthermore, the egg ball is very popular in Groningen. It is a kind of breaded deep-fried ball containing a whole or half hard-boiled egg. You can buy them at several snack bars. 
