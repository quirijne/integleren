Years ago, The Conference Board of Canada, an independent, not-for-profit applied research organization, developed the Employability Skills 2000+,
 which lists the critical skills that employees need to succeed in the workplace. 
 Communication skills, tops the list of fundamental skills needed to succeed in the workplace. 
  <hr>
<p>
A decade-and-a-half later, with the rise of social media networking and texting, 
communication is becoming more casual, even in situations where more formal ways of communicating are required.
 What this means is that people from the younger generation, may not know or even understand the importance of effective 
 communication skills in the workplace. When you take a look at the greatest leaders, one of the traits they possess, is 
 the ability to communicate effectively, which underscores the importance of communication skills. 
</p>
 
<hr>
<p>
There are specific things to do that can improve your communication skills: 
</p>
 
<p>
 Listen, listen, and listen. People want to know that they are being heard.
 Really listen to what the other person is saying, instead of formulating your response.
 Ask for clarification to avoid misunderstandings. At that moment, the person speaking to you should be the most 
 important person in your life. Another important point is to have one conversation at a time.
 This means that if you are speaking to someone on the phone, do not respond to an email, or send a text at the same time.
 The other person will know that she doesnâ€™t have your undivided attention. 
</p>
 <hr>
<p>
Who you are talking to matters. 
It is okay to use acronyms and informal language when you are communicating with a buddy, 
but if you are emailing or texting your boss, "Hey," "TTYL" or any informal language, has no place in your message.
 You cannot assume that the other person knows what the acronym means. Some acronyms have different meanings to 
 different people, do you want to be misunderstood? Effective communicators target their message based on who they 
 are speaking to, so try to keep the other person in mind, when you are trying to get your message across. 
</p>
 <hr>
<p>
Body language matters. This is important for face-to-face meetings and video conferencing.
 Make sure that you appear accessible, so have open body language. This means that you should not cross your arms.
 And keep eye contact so that the other person knows that you are paying attention. 
</p>
 <hr>
<p>
Check your message before you hit send. Spell and grammar checkers are lifesavers, 
but they are not foolproof. Double check what you have written, to make sure that your words are
communicating the intended message. 
</p>
 <hr>
<p>
Be brief, yet specific. For written and verbal communication, practice being brief yet specific enough, 
that you provide enough information for the other person to understand what you are trying to say.
 And if you are responding to an email, make sure that you read the entire email before crafting your response. 
 With enough practice, you will learn not to ramble, or give way too much information. 
</p>
 <hr>
<p>
Write things down. Take notes while you are talking to another person or when you are in a meeting,
 and do not rely on your memory. Send a follow-up email to make sure that you understand what was being said during 
 the conversation. 
</p>
 <hr>
<p>
Sometimes it's better to pick up the phone. If you find that you have a lot to say, instead of sending an email, 
call the person instead. Email is great, but sometimes it is easier to communicate what you have to say verbally. 
</p>
 <hr>
<p>
Think before you speak. Always pause before you speak, not saying the first thing that comes to mind.
 Take a moment and pay close attention to what you say and how you say it. This one habit will allow you to avoid 
 embarrassments. 
 </p>
 <hr>
<p>
Treat everyone equally. Do not talk down to anyone, treating everyone with respect. Treat others as your equal. 
</p>
 <hr>
<p>
Maintain a positive attitude and smile. Even when you are speaking on the phone, smile because your positive 
attitude will shine through and the other person will know it. When you smile often and exude a positive attitude,
 people will respond positively to you. 
</p>
 <hr>
<p>
Communicating effectively is a teachable skill, therefore following a few of the tips outlined above, 
will enable you to hone up on your communication skills. 
</p>
 
